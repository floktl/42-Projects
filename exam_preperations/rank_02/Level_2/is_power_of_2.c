#include <unistd.h>

int	is_power_of_2(unsigned int n)
{
	if (n > 0)
	{
		return ((n & (n - 1)) == 0);
	}
	return (0);
}

int	main(void)
{
	unsigned int	i;
	char			f;

	i = 1;
	f = is_power_of_2(i) + '0';
	write(1, &f, 1);
	return (0);
}

